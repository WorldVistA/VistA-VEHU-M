 ; 16=CANCELLED BUT REPORTED AS ORND WITHDRAWN
 I FCLST=16 S ORNDC=ORNDC+1 Q
 ; THESE ARE ALL OTHER NON DISCLOSURES
 ; 6=NO RECORD
 I FCLST=6 S ORNDA=ORNDA+1 Q 
