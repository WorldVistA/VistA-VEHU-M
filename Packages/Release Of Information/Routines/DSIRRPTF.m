 ; 21=PATIENT DIED
 I FCLST=21 S ORNDK=ORNDK+1 Q
 ; 22=PUBLICLY AVAILABLE
 I FCLST=22 S ORNDL=ORNDL+1 Q
 ; 23=GLOMAR
 I FCLST=23 S ORNDM=ORNDM+1 Q
 ; 24=SUBSUMED BY LITIGATION
 I FCLST=24 S ORNDN=ORNDN+1 Q
 ; CLOSED PARTIAL/DENIED WITH EXEMPTIONS
