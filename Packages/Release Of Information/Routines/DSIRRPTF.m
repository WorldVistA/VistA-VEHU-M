 ; PARTIAL REQUESTS
 I FCLST=5 S NBRDENY=NBRDENY+1
 ; DENIED REQUESTS
 ;
 ;EXEMPTION PROCESSING
 I (FCLST=4)!(FCLST=5) D 
 .S:$P(ROI12,U,1) EXEMP1=EXEMP1+1 S:$P(ROI12,U,2) EXEMP2=EXEMP2+1
 .I +$P(ROI12,U,3) D
 ..;IF EXEMPTION 3 THEN INCREMENT AND PROCESS STATUTES
 ..S EXEMP3=EXEMP3+1
 ..S:$P(ROI13,U) EX35701=EX35701+1 S:$P(ROI13,U,2) EX35705=EX35705+1
 ..S:$P(ROI13,U,3) EX3205=EX3205+1 S:$P(ROI13,U,4) EX37332=EX37332+1
 ..S:$P(ROI13,U,11) EX3253B=EX3253B+1 S:$P(ROI13,U,12) EX3APP3=EX3APP3+1
 .S:$P(ROI12,U,4) EXEMP4=EXEMP4+1 S:$P(ROI12,U,5) EXEMP5=EXEMP5+1
 .S:$P(ROI12,U,6) EXEMP6=EXEMP6+1 S:$P(ROI12,U,7) EXEMP7A=EXEMP7A+1
 .S:$P(ROI12,U,8) EXEMP7B=EXEMP7B+1 S:$P(ROI12,U,9) EXEMP7C=EXEMP7C+1
 .S:$P(ROI12,U,10) EXEMP7D=EXEMP7D+1 S:$P(ROI12,U,11) EXEMP7E=EXEMP7E+1
 .S:$P(ROI12,U,12) EXEMP7F=EXEMP7F+1 S:$P(ROI12,U,13) EXEMP8=EXEMP8+1
 .S:$P(ROI12,U,14) EXEMP9=EXEMP9+1
 Q
