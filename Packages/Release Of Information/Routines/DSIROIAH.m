 ;      ===>RPT GROUP TITLE FONT 1^RPT GROUP TITLE SIZE 1^
 ;      ===>RPT GROUP TITLE BOLD 1^RPT GROUP TITLE ITALICS 1^
 ;      ===>RPT GROUP TITLE UNDERLN 1
