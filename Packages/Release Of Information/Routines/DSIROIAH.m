 ;Return Value
 ;ARRAY:- AS FOLLOWS:
 ; [1]  RPT NAME^RPT AUTHOR^RPT CREATION DATE^RPT LAST UPDATED
 ; [2]  RPT SELECTION FIELD 1^RPT SELECTION VALUE 1
 ; [3]  RPT SELECTION FIELD 2^RPT SELECTION VALUE 2
 ; [4]  RPT SELECTION FIELD 3^RPT SELECTION VALUE 3
