 ;      ===>RPT SORT COUNT 3 RPT SORT SUM 3
 ; [10] RPT TITLE^RPT TITLE REPEAT^RPT TITLE FONT^RPT TITLE SIZE^
 ;      ===>RPT TITLE BOLD^RPT TITLE ITALICS^RPT TITLE UNDERLN
