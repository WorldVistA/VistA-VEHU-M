IDENTIFYGHOST() ;
 N CLINICIEN,GHOST,CLINICIEN,CLINICNAME,COUNT,NONSTDDATE,INDEFDATE,STARTDATE,ORPHANDATE
 ;
 S CLINICIEN=0
 F  S CLINICIEN=$O(^SC(CLINICIEN)) Q:'CLINICIEN  D
 .I $$INACTIVE^SDESUTIL(CLINICIEN) Q
 .;
 .S STARTDATE=$$FMADD^XLFDT(DT,-$$GET1^DIQ(44,CLINICIEN,2002))
 .;
 .D INDEFOVERWRITES(.GHOST,CLINICIEN,STARTDATE)
 .D ORPHANEDOSTDAYS(.GHOST,CLINICIEN)
 ;
 ;
 K ^XTMP("SDES942P")
 S COUNT=1
 S ^XTMP("SDES942P",COUNT)="**********************************GHOST AVAILABILITY REPORT**********************************"
 S COUNT=COUNT+1
 S ^XTMP("SDES942P",COUNT)="CLINIC^ISSUE DESCRIPTION^DATE"
 ;
 S CLINICIEN=0
 F  S CLINICIEN=$O(GHOST(CLINICIEN)),CLINICNAME=$$GET1^DIQ(44,CLINICIEN,.01) Q:'CLINICIEN  D
 .;
 .S INDEFDATE=0
 .F  S INDEFDATE=$O(GHOST(CLINICIEN,"INDEFINITE OVERWRITE",INDEFDATE)) Q:'INDEFDATE  D
 ..S COUNT=COUNT+1
 ..S ^XTMP("SDES942P",COUNT)=CLINICNAME_U
 ..S ^XTMP("SDES942P",COUNT)=^XTMP("SDES942P",COUNT)_"Indefinite availablity is overlayed with Special availablity"_U_$$FMTISO^SDAMUTDT(INDEFDATE)
 .;
 .S ORPHANDATE=0
 .F  S ORPHANDATE=$O(GHOST(CLINICIEN,"ORPHANED OST",ORPHANDATE)) Q:'ORPHANDATE  D
 ..S COUNT=COUNT+1
 ..S ^XTMP("SDES942P",COUNT)=CLINICNAME_U
 ..S ^XTMP("SDES942P",COUNT)=^XTMP("SDES942P",COUNT)_"Unable to define precise time for special availability defined"_U_$$FMTISO^SDAMUTDT(ORPHANDATE)
 ;
 Q
 ;
INDEFOVERWRITES(GHOST,CLINICIEN,DATE) ;
 N TNODE,DONE
 ;
 F TNODE=0:1:6 I $D(^SC(CLINICIEN,"T"_TNODE)) D
 .;
 .S DATE=0,DONE=0
 .F  S DATE=$O(^SC(CLINICIEN,"T"_TNODE,DATE)) Q:'DATE!(DONE)  D
 ..;
 ..I $O(^SC(CLINICIEN,"T"_TNODE,DATE))=9999999,$D(^SC(CLINICIEN,"OST",DATE)) D
 ...S GHOST(CLINICIEN,"INDEFINITE OVERWRITE",DATE)=""
 ...S DONE=1
 Q
 ;
ORPHANEDOSTDAYS(GHOST,CLINICIEN) ;
 N DATE
 ;
 S DATE=$$FMADD^XLFDT(DT,-1)
 F  S DATE=$O(^SC(CLINICIEN,"OST",DATE)) Q:'DATE  D
 .I '$D(^SC(CLINICIEN,"T",DATE)) D
 ..S GHOST(CLINICIEN,"ORPHANED OST",DATE)=""
 Q
