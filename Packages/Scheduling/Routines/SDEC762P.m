 ;
 ; change the verison number and date in the SDEC SETTINGS File (#409.98)
FIND ;FIND THE IEN FOR "VS GUI NATIONAL"
 N SDECDA,SDECDA2
 W !!?3,"Updating SDEC SETTINGS file (#409.98)",!!
 D VERSION   ;update GUI version number and date
 G XIT
VERSION ;SET THE NEW VERSION UPDATE IN SDEC SETTING FILE #409.98 TO 1.7.1
 S SDECDA=0,SDECDA=$O(^SDEC(409.98,"B","VS GUI NATIONAL",SDECDA)) G:$G(SDECDA)="" NOFIND   ;get DA for VS GUI NATIONAL
 S DA=SDECDA,DIE=409.98,DR="2///1.7.0.2 R1;3///3200730" D ^DIE  ;update VS GUI NATIONAL
 K DIE,DR,DA
 S SDECDA2=0,SDECDA2=$O(^SDEC(409.98,"B","VS GUI LOCAL",SDECDA2)) Q:$G(SDECDA2)=""    ;get DA for VS GUI LOCAL
 S DA=SDECDA2,DIE=409.98,DR="2///1.7.0.2 R1;3///3200730" D ^DIE  ;update VS GUI LOCAL
 K DIE,DR,DA
 Q
NOFIND ;"VS GUI NATIONAL" NOT FOUND
 W !!?3,"VS GUI NATIONAL not found in the SDEC SETTINGS file (#409.98)"
XIT ;EXIT ROUTINE
 Q
