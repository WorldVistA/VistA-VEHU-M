TASK ; tasks off process to run the Missing Clinic "S" Nodes Report
 N %,%I
 D MES^XPDUTL("")
 D MES^XPDUTL(" SD*5.3*943 Post-Install to create report of")
 D MES^XPDUTL("  missing 'S' nodes in the HOSPITAL LOCATION (#44) file.")
 D MES^XPDUTL("")
 N ZTDESC,ZTRTN,ZTIO,ZTSK,X,ZTDTH,ZTSAVE
 S ZTDESC="SD*5.3*943 Post Install Routine Task 1"
 D NOW^%DTC
 S ZTDTH=$P($H,",",1)_","_86399,ZTIO="",ZTRTN="REPORT^SDES943P",ZTSAVE("*")=""
 D ^%ZTLOAD
 I $D(ZTSK) D
 . D MES^XPDUTL(" >>>Task "_ZTSK_" has been queued.")
 . D MES^XPDUTL("")
 I '$D(ZTSK) D
 . D MES^XPDUTL(" UNABLE TO QUEUE THIS JOB.")
 . D MES^XPDUTL(" Please contact the National Help Desk to report this issue.")
 Q
 ;
REPORT ;
 K ^XTMP("SDES943P")
 S ^XTMP("SDES943P",0)=$$FMADD^XLFDT(DT,30)_"^"_DT_"^SD*5.3*943"
