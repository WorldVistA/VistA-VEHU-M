 S STARTDATE=$$FMADD^XLFDT(DT,-91)_".999999"
 ;
 S (COUNT,SDPATIENT)=0
 S COUNT=COUNT+1
 S ^XTMP("SDES943P",COUNT)="*********MISSING 'S' NODES REPORT*********"
