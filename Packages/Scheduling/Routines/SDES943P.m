 . I $D(RETURN) D
 . . S INDEX=0 F  S INDEX=$O(RETURN(INDEX)) Q:'INDEX  D
 . . . S COUNT=COUNT+1
 . . . S ^XTMP("SDES943P",COUNT)=RETURN(INDEX)
 . K RETURN
 ;
