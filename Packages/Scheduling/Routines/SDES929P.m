 S XMY("DUNNAM.DAVID@FORUM.DOMAIN.EXT")=""
 S XMY("CRUZ.ORLANDO@FORUM.DOMAIN.EXT")=""
 S XMY("BUTLER.BRANDON@FORUM.DOMAIN.EXT")=""
 D ^XMD
 Q
