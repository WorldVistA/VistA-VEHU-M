GETINDEFSLOTDATE(CLINICIEN,DATE,TNODE) ;
 N TDATE,INDEFDATE
 ;
 S INDEFDATE=0
 F  S DATE=$O(^SC(CLINICIEN,"T",DATE),-1) Q:'DATE!($G(INDEFDATE))  D
 .I $$DOW^XLFDT(DATE,1)=$E(TNODE,2) D
 ..I $D(^SC(CLINICIEN,"OST",DATE)) Q
 ..S INDEFDATE=DATE
 Q INDEFDATE
