REMOVECOMMENTS(TEXT) ; Remove comments but not URLs
 N LINE,POS,DONE
 S LINE=TEXT
 S POS=1,DONE=0 F  D  Q:DONE
 . S DONE=1,POS=$F(LINE,"//",POS) I POS>0 D
 .. I $E(LINE,POS-3)=":" S DONE=0
 .. E  S LINE=$E(LINE,1,POS-3)
