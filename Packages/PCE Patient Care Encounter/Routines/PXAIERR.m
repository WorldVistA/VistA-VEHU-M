NUMWARNINGS(PXAERR) ;Calculate the number of warnings that are defined.
 N NUMW,SUB
 S NUMW=0,SUB=""
 F  S SUB=$O(PXAERR(SUB)) Q:SUB=""  D
 . I SUB["W" S NUMW=NUMW+1
 Q NUMW
