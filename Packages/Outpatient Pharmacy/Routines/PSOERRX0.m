 S DIR(0)=DIR(0)_";5:ERX STATUS" I $G(STSFLTR) S DIR(0)=DIR(0)_" "_IOINHI_"("_$S($G(STSFLTR1)'="":STSFLTR1,1:$$GET1^DIQ(52.45,+STSFLTR,.01))_")"_IOINORM
