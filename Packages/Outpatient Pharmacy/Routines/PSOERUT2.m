 . S UNDERLN(LINE,41)=2
 . D ADDLINE^PSOERUT0(MODE,NMSPC,"",XV)
 . D BLANKLN^PSOERUT0(MODE)
 ;
 ; - Quantity
 S XE="Quantity: "_$$COMPARE^PSOERUT0(MODE,ERXQTY,VAQTY,11,,,'VADRGIEN)
