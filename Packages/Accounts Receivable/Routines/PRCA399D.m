 S PRCAAC=+$O(^PRCA(430.3,"AC",102,0)),PRCAOP=+$O(^PRCA(430.3,"AC",112,0)),PRCARVW=+$O(^PRCA(430.3,"AC",113,0))
 S:$G(PRCABEG)="" PRCABEG=0 I +$G(PRCAEND)=0 D NOW^%DTC S PRCAEND=%
 S PRCABEG=0
 S PRCATTY=$G(PRCATTY) I PRCATTY="" D F430
 D F433
Q Q PRCATTY
F430 W:PRCAW !,"F430"
