 S PRCADAT=PRCABEG F  S PRCADAT=$O(^PRCA(430,"ATD",PRCADB,PRCADAT)) Q:('PRCADAT)!(PRCADAT>PRCAEND)  S PRCABN=0 F  S PRCABN=$O(^PRCA(430,"ATD",PRCADB,PRCADAT,PRCABN)) Q:'PRCABN  D
 .Q:'$D(^XTMP("PRCA399",$J,1,1,PRCABN))!$D(^XTMP("PRCA399",$J,5,PRCADB,PRCABN,46))
 .S PRCASTS=$P(^PRCA(430,PRCABN,0),U,8) I PRCASTS=PRCAAC!(PRCASTS=PRCAOP)!(PRCASTS=PRCARVW) D
 ..I $D(^XTMP("PRCA399",$J,1,1,PRCABN)),$P(^PRCA(430,PRCABN,0),U,3) S ^TMP("PRCAGT",$J,PRCADB,PRCADAT,PRCABN,99,1)=$P(^PRCA(430,PRCABN,0),"^",3)_"^1^"_PRCABN_U_"ORIG AMT"
 Q
F433 ;
 K ^PRCAGT("PRCAGT",$J)
 N PRCAPREP,PRCAPRPY
 W:PRCAW !,"F433"
 S PRCACOMM=0,PRCAPREP=+$O(^PRCA(430.2,"AC",33,0)),PRCASKIP=0
 F PRCADAT=PRCABEG:0 S PRCADAT=$O(^PRCA(433,"ATD",PRCADB,PRCADAT)) Q:('PRCADAT)!(PRCADAT>PRCAEND)  F PRCATN=0:0 S PRCATN=$O(^PRCA(433,"ATD",PRCADB,PRCADAT,PRCATN)) Q:'PRCATN  D
CK1 .S PRCATN0=$G(^PRCA(433,PRCATN,0)) Q:PRCATN0=""  S PRCATN1=$G(^PRCA(433,PRCATN,1)),PRCADT=$P(PRCATN1,U,9)
 .Q:'$P(PRCATN1,U,2)!'$P(PRCATN1,U,5)
 .S PRCABILL=$P(PRCATN0,U,2),PRCAPRPY=0,PRCAPULL=0 Q:'PRCABILL
 .I $P(^PRCA(430,PRCABILL,0),U,8)=31 Q
 .I '$D(^XTMP("PRCA399",$J,1,1,PRCABILL)) Q
CK2 .I $D(^XTMP("PRCA399",$J,5,PRCADB,PRCABILL,46)) Q:$P(PRCATN1,U,9)<^XTMP("PRCA399",$J,5,PRCADB,PRCABILL,46)
 .I $D(^XTMP("PRCA399",$J,2,1,PRCADB,1,PRCABILL,2,PRCATN)) S $P(PRCATN1,U,5)=$P(PRCATN1,U,5)-^XTMP("PRCA399",$J,2,1,PRCADB,1,PRCABILL,2,PRCATN)
 .S PRCATYPE=$P(PRCATN1,U,2),PRCATAMT=$P(PRCATN1,U,5)
 .I ":199:47:45:8:"[(":"_PRCATYPE_":") Q
 .I PRCATYPE=14,PRCATAMT<0 S PRCATYPE=13,PRCATAMT=$TR(PRCATAMT,"-")
 .I PRCATYPE=12,PRCATAMT<0 S PRCATAMT=$TR(PRCATAMT,"-")
 .I PRCATYPE=2,$P(^XTMP("PRCA399",$J,2,1,0),U,3)=528,$P(PRCATN1,U,3)="P17011000" Q
CK3 .I '$D(^XTMP("PRCA399",$J,1,1,PRCABILL)),$P($G(^PRCA(430,PRCABILL,6)),U,21)<$P(^XTMP("PRCA399",$J,2,1,0),U),$P(PRCATN1,U,9)>$P(^XTMP("PRCA399",$J,2,1,0),U) D
 ..Q:",2,7,9,10,11,14,19,29,34,35,41,"'[(","_PRCATYPE_",")!($P(PRCATN0,U,10)=1)
 ..S $P(^XTMP("PRCA399",$J,2,8,PRCADBTR,0),U)=$P(^XTMP("PRCA399",$J,2,8,PRCADBTR,0),U)+$P(PRCATN1,U,5)
 ..S $P(^XTMP("PRCA399",$J,2,1,PRCADBTR,2,PRCABILL,0),":::")=$P(PRCATN1,U,5)
 ..S ^XTMP("PRCA399",$J,2,8,PRCADBTR,PRCABILL,2)=$P(PRCATN1,U,5),$P(^XTMP("PRCA399",$J,2,1,PRCADBTR,2,PRCABILL,0),":::",1)=$P(PRCATN1,U,5)
 .I $P($G(^XTMP("PRCA399",$J,1,1,PRCABILL,0)),U,2)=PRCAPREP S PRCAPRPY=1   ;@@@@@@@@@@@@@@
 .I $P(PRCATN0,U,10)=1,'$D(^XTMP("PRCA399",$J,1,1,PRCABILL)) S ^XTMP("PRCA399",$J,2,6,PRCABILL,PRCATN)=PRCATN0_U_PRCATYPE_U_PRCATAMT Q
 .I PRCATYPE=14,PRCATAMT<0 S PRCATYPE=13,$P(PRCATN1,U,5)=$TR(PRCATAMT,"-")
 .W:PRCAW !,PRCATN,?12,PRCATN0,?52,PRCATN1
 .I $G(PRCATTY)'="",PRCATTY'=PRCATYPE Q
 .I PRCATTY="",",3,4,5,6,7,24,25,30,90,91,92,"[(","_PRCATYPE_",") Q
 .I (PRCATYPE="")!($P(PRCATN0,U,4)'=2) Q    ;*******
 .I $G(PRCAHIST)="THIST",PRCATYPE=45 Q
F433A .S ^TMP("PRCAGT",$J,PRCADB,PRCADAT,PRCABILL,99,PRCATN)=+PRCATAMT_U_PRCATYPE_U_PRCABILL_U_PRCAPRPY_U_PRCATN
 .I PRCATAMT["-" S ^XTMP("PRCA399",$J,8,5,PRCADB,PRCABILL,PRCATN,0)=PRCATAMT_U_PRCATYPE_U_PRCABN_U_PRCAPRPY
 S PRCADAT=0 F  S PRCADAT=$O(^TMP("PRCAGT",$J,PRCADB,PRCADAT)) Q:'PRCADAT  S PRCAEND=PRCADAT
 K PRCADAT,PRCATN,PRCAPRCATN0,PRCATN1,PRCACOMM,PRCATYPE,PRCATTYP
