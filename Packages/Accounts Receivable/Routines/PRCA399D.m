 I 'PRCACSFL K PRCACSCH,PRCACSPC
 K PRCABN,PRCACH,PRCADAT,PRCAPC,PRCARF,PRCARR,PRCATAMT,PRCATXN,PRCACS,PRCACSFL,^TMP("PRCAGU",$J),^TMP("PRCAGT",$J)
 K PRCAAC,PRCABILL,PRCACST,PRCACSTC,PRCACTCH,PRCAHIST,PRCAHTMT,PRCAOP,PRCAPNEG,PRCAQ,PRCASTS
 K PRCATN0,PRCATOT,PRCAI,PRCANFLG,PRCAZ,PRCAPULL,PRCARVW
 Q PRCATTY
CHK ;CHK TOTALS
 ;W !,PRCATAMT,?30,PRCARF_U_PRCACH_U_PRCAPC_U_(PRCARF+PRCACH+PRCAPC),?60,PRCAPBAL," / ",(PRCARF+PRCACH+PRCAPC)," / ",PRCABBAL
 ;S ^BZ2(1,PRCADB,PRCABN,PRCADAT,PRCATXN,1)=PRCARF_U_PRCACH_U_PRCAPC_U_(PRCARF+PRCACH+PRCAPC)
