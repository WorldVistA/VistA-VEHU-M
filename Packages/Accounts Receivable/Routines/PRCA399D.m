 S (PRCATBAL,PRCAPC,PRCARF,PRCARR,PRCACH,PRCAZ,PRCAW,PRCATN,PRCACHH)=0
 S PRCARR=+$O(^PRCA(430.2,"AC",33,0)),(PRCATOT,PRCAHTMT,PRCATTY)=0,PRCACSFL=$D(PRCACSTC),PRCAPNEG=0
 I $D(^XTMP("PRCA399",$J,2,1,PRCADB,0)),($P(^XTMP("PRCA399",$J,2,1,PRCADB,0),U)+$P(^XTMP("PRCA399",$J,2,1,PRCADB,0),U,2)>0) D
 .F PRCAI=1,2 S PRCAPC=PRCAPC-$P(^XTMP("PRCA399",$J,2,1,PRCADB,0),U,PRCAI)
 .W:PRCAW !,"PRENEG HIT: ",^XTMP("PRCA399",$J,2,1,PRCADB,0) S PRCAPNEG=^XTMP("PRCA399",$J,2,1,PRCADB,0) R:PRCAW PRCAZ:10
 I '$D(^TMP("PRCAGT",$J,PRCADB)) G TBALQ
 F PRCADAT=0:0 S PRCADAT=$O(^TMP("PRCAGT",$J,PRCADB,PRCADAT)),PRCABN=0 Q:'PRCADAT  F  S PRCABN=$O(^TMP("PRCAGT",$J,PRCADB,PRCADAT,PRCABN)) Q:'PRCABN  D
 .S PRCACS=$D(^PRCA(430,"TCSP",PRCABN)) ; set flag for CS bills
 .I $D(^TMP("PRCAGT",$J,PRCADB,PRCADAT,PRCABN,0)) S PRCACH=PRCACH+^(0) S:PRCACS PRCACTCH=$G(PRCACTCH)+^(0)
 .S PRCATXN=0 F  S PRCATXN=$O(^TMP("PRCAGT",$J,PRCADB,PRCADAT,PRCABN,99,PRCATXN)) Q:'PRCATXN  D
 ..S PRCATAMT=^(PRCATXN),PRCATTY=$P(PRCATAMT,U,2),PRCAPRPY=$P(PRCATAMT,U,4) W:PRCAW !,PRCATXN,"/",$P(^PRCA(433,PRCATXN,0),U,2),?24,PRCATAMT
 ..I PRCATTY'=45 D
 ...I PRCATAMT["-" S PRCANFLG=1,^XTMP("PRCA399",$J,2,1,PRCADB,3,PRCABN,PRCATXN)=+PRCATAMT
 ...I PRCATTY=12!(PRCATTY=74) S:PRCATAMT<0 PRCAPC=PRCAPC+PRCATAMT D CHK S:PRCATAMT'<0 PRCACH=PRCACH+PRCATAMT S:PRCATAMT<0&PRCACS PRCACST=$G(PRCACST)+PRCATAMT S:PRCATAMT'<0 PRCACST=$G(PRCACST)+PRCATAMT D W
 ...;  interest and admin charges may be negative
 ...;  this was added in patch 165
 ...I PRCAPRPY,":1:41:35:"[(":"_PRCATTY_":") S:PRCATTY=1 PRCAPC=PRCAPC-PRCATAMT S:(PRCATTY=41!(PRCATTY=35)) PRCARF=PRCARF+PRCATAMT D CHK Q
 ...I ",2,7,9,10,11,14,19,29,34,35,47,"[(","_PRCATTY_",") S PRCAPC=PRCAPC-PRCATAMT S:PRCACS PRCACS=$G(PRCACS)-PRCATAMT D CHK D W Q
 ...I ",1,13,43,46,73,"[(","_PRCATTY_",") S PRCACH=PRCACH+PRCATAMT S:PRCACS PRCACS=$G(PRCACS)+PRCATAMT D W D CHK
 ;
