PRCA399F ;MNTVBB/RGB - Restores any changes to AR files to allow re-testing ; FEB 05, 2022
 ;;4.5;Accounts Receivable;**399**;;Build 67
 ;;Per VA Directive 6402, this routine should not be modified.
 Q
EN I '$D(^XTMP("PRCA399",$J,998)) W !!,"*********THERE IS NO PENDING RESTORE DATA, ALREADY DONE ********" Q
 D A1,A2,A5,A12,A14,A16 Q
A1 ;RESTORE FOR REMOVED EVENT FILE ENTRIES IN 399 CALC
 Q   ;NO LONGER REMOVE PREVIOUS EVENTS!!
 S U="^",PRCADB=0,XX=0      ;^XTMP("PRCA399",$J,9,9,PRCACTR)=PRCA341
 F  S PRCADB=$O(^XTMP("PRCA399",$J,9,9,PRCADB)),PRCACTR=0 Q:'PRCADB  D
 .F  S PRCACTR=$O(^XTMP("PRCA399",$J,9,9,PRCADB,PRCACTR)) Q:'PRCACTR  D
 ..S DA=PRCACTR,DIK="^RC(341," D IX^DIK ;I 'XX W !,PRCADB," / ",PRCACTR R:'XX XX:25
 W !,"ALL PRCA399 CALC SET 341 ENTRIES BACKED OUT**"
 K PRCACTR,DA,DIK,XX
 Q
 ;
A2 ;Remove new file 341 entries set during install
 S U="^",PRCADB=0,XX=0
 F  S PRCAIEN=$O(^XTMP("PRCA399",$J,9,9,PRCADB)),PRCACTR=0 Q:'PRCADB  D
 .F  S PRCACTR=$O(^XTMP("PRCA399",$J,9,9,PRCADB,PRCACTR)) Q:'PRCACTR  D
 ..S DA=PRCACTR,DIK="^RC(341," D ^DIK   ;I 'XX W !,PRCACTR R:'XX XX:25
 ;W !!,"==> new EVENT FILE (431) entry "_PRCACTR_" removed"
 K PRCAIEN,PRCACTR,DA,DIK,XX
 Q
 ;
A5 ;Reset flipped cd 14 tx in file 433 to before testing status
 W !!,"Reset transaction type 14 with negative amount",!
 S U="^",PRCAIEN=0,XX=0
 F  S PRCAIEN=$O(^XTMP("PRCA399",$J,7,2,PRCAIEN)) Q:'PRCAIEN  D
 .S PRCATXN1=^XTMP("PRCA399",$J,7,2,PRCAIEN,1) W !,PRCAIEN,?12,PRCATXN1
 .S ^PRCA(433,PRCAIEN,1)=PRCATXN1
 K PRCAIEN,PRCATXN1,XX
 W !!,"Original flipped 14 nodes restore to before instance"
 Q
 ;
A12 ;RESTORE ORIGINAL NODE 7 FROM ^XTMP("PRCA399",$J,7,3,,"7A")
 S U="^",PRCABILL=0,XX=0
 F  S PRCABILL=$O(^XTMP("PRCA399",$J,7,3,PRCABILL)) Q:'PRCABILL  D
 .S PRCAOLD7=^XTMP("PRCA399",$J,7,3,PRCABILL,"7A"),^PRCA(430,PRCABILL,7)=PRCAOLD7
 K PRCABILL,PRCAOLD7,XX
 W !!,"Original adj transactions restore to before instance"
 Q
 ;
A14 ;reset original tx from tag 14 to before testing update
 S U="^",PRCADB=0,XX=0
 F  S PRCADB=$O(^XTMP("PRCA399",$J,2,1,PRCADB)),PRCABILL=0 Q:'PRCADB  D
 .F  S PRCABILL=$O(^XTMP("PRCA399",$J,2,1,PRCADB,4,PRCABILL)),PRCATX=0 Q:'PRCABILL  D
 ..F  S PRCATX=$O(^XTMP("PRCA399",$J,2,1,PRCADB,4,PRCABILL,PRCATX)) Q:'PRCATX  D
 ...S ^PRCA(433,PRCATX,1)=$P(^XTMP("PRCA399",$J,2,1,PRCADB,4,PRCABILL,PRCATX),":",3)
 ...W !,"RESTORED: ",PRCATX,?22,$P(^XTMP("PRCA399",$J,2,1,PRCADB,4,PRCABILL,PRCATX),":",3)
 W !!,"Restored modified transaction amts from leftover decrease or prepay"
 K PRCADB,PRCABILL,PRCATX,XX
 K ^XTMP("PRCA399",$J,998)   ;PREVENTS BACK TO BACK RUNS BY SETTING IN PRCA399P, BUT ALLOWS RUN ONCE RESTORED
 Q
 ;
A16 ;SET OLD 2,3,8 NODE BACK INTO FILE 433
 S PRCADEB=0,Z="/",XX=0,PRCAREST=0,T=0
 ;PRCAT238=PRIN;INT;FEE;NODE#;TX CD;TX CHG;DIFF BETWEEN TX CHG AND NODE TOT;FLIP
 ;S ^XTMP("PRCA399",$J,4,1,PRCADEB,PRCABILL,PRCATX,PRCATXN,1)=PRCAT238
 ;S ^XTMP("PRCA399",$J,4,1,PRCADEB,PRCABILL,PRCATX,PRCATXN,2)=PRCAT238.DIFF.FLIP?
 F  S PRCADEB=$O(^XTMP("PRCA399",$J,4,1,PRCADEB)),PRCABILL=0 Q:'PRCADEB  D
 .F  S PRCABILL=$O(^XTMP("PRCA399",$J,4,1,PRCADEB,PRCABILL)),PRCATX=0 Q:'PRCABILL  D
 ..F  S PRCATX=$O(^XTMP("PRCA399",$J,4,1,PRCADEB,PRCABILL,PRCATX)),PRCATYP=0 Q:'PRCATX  D
 ...F  S PRCATYP=$O(^XTMP("PRCA399",$J,4,1,PRCADEB,PRCABILL,PRCATX,PRCATYP)) Q:'PRCATYP  D
 ....S PRCAT1=^XTMP("PRCA399",$J,4,1,PRCADEB,PRCABILL,PRCATX,PRCATYP,1)
 ....S PRCAT2=^XTMP("PRCA399",$J,4,1,PRCADEB,PRCABILL,PRCATX,PRCATYP,2)
 ....S PRCAREST=0
 ....F I=1:1:3 I +$P(PRCAT1,U,I)'=+$P(PRCAT2,U,I) S PRCAREST=1 D
 .....I PRCATYP=2 F I=2,3 S $P(^PRCA(433,PRCATX,2),U,I+5)=+$P(PRCAT1,U,I)
 .....I PRCATYP'=2 F I=1:1:3 S $P(^PRCA(433,PRCATX,PRCATYP),U,I)=+$P(PRCAT1,U,I)
 .....;I PRCAREST W !,PRCADEB,Z,PRCABILL,Z,PRCATX,Z,PRCATYP W ?30,PRCAT1,!,?30,PRCAT2 R:'XX XX
 K PRCADB,PRCABILL,PRCATX,PRCATYP,PRCAT1,PRCAT2,PRCAFIX
 Q
 ;
A17 ;RESTORE 433 NODES MODIFIED DUE TO PREPAY/PNEG TO BALANCE BILL
B190 ;TEST DISCREPS
 S U="^"
B191 R !,"DEBTOR IEN: ",PRCADEB:30 Q:'PRCADEB
 S PRCABN=0
 F  S PRCABN=$O(^PRCA(430,"C",PRCADEB,PRCABN)) G B192:'PRCABN D
 .S PRCAX=$$GETTRANS^RCDPBTLM(PRCABN),PRCAY=$P($G(^PRCA(430,PRCABN,7)),U,1,5)
 .X "S PRCAOK=1 F PRCAPCE=1:1:5 I +$P(PRCAX,U,PRCAPCE)'=+$P(PRCAY,U,PRCAPCE) S PRCAOK=0 Q"
 .I 'PRCAOK W !,PRCABN,?9,$P($G(^PRCA(430,PRCABN,0)),U,1),?30,PRCAX,?50,PRCAY
 G B191
B192 K PRCADEB,PRCABN,PRCAY,PRCAX,PRCAOK,PRCAPCE
 Q
X1 ;Gather 433 tx with zero amt
 S PRCATX=0,PRCATX=0
 F  S PRCATX=$O(^PRCA(433,PRCATX)) Q:'PRCATX  D
 .S PRCAR=$G(^PRCA(433,PRCATX,1))
 .I $P(PRCAR,U,2)'=45,$P(PRCAR,U,5)=0 S ^XTMP("PRCA399",$J,5,1,PRCATX)=PRCAR
 .W !,PRCATX,?15,PRCAR R:'PRCAX PRCAX:10
 K PRCATX,PRCAOLD7,PRCAR,PRCAX
 Q
 ;
X2 ;CHECK ^XTMP("PRCA399",$J,5,1 ZERO AMT ENTRIES AGAINST SUB NODES
 S U="^",PRCAX=0
 R !,"STARTING TX IEN: ",PRCATX:30 Q:'PRCATX
 F  S PRCATX=$O(^XTMP("PRCA399",$J,5,1,PRCATX)) Q:'PRCATX  D
 .S PRCATXB="NONE",PRCATXN=^XTMP("PRCA399",$J,5,1,PRCATX),PRCACD=$P(PRCATXN,U,2) Q:PRCACD=45  W:PRCAX !,PRCATX,?11,PRCATXN
 .I ";12;13;14;"[(";"_PRCACD_";") S PRCATXB=$G(^PRCA(433,PRCATX,2))
 .I ";2;34;"[(";"_PRCACD_";") S PRCATXB=$G(^PRCA(433,PRCATX,3))
 .I ";8;9;10;11;35;43;46;47;"[(";"_PRCACD_";") S PRCATXB=$G(^PRCA(433,PRCATX,8))
 .;W ?58,PRCATXB R:'PRCAX PRCAX:10
 K PRCAX,PRCATX,PRCATXB,PRCATXN,PRCACD
 Q
 ;
TRACEON ;Turn tracing ON to watch calcs
 S ^XTMP("PRCA399",$J,1,0)=1
 S ^XTMP("PRCA399",$J,2,0)=1
 Q
TRACEOFF ;Turn tracing OFF to NOT watch calcs
 S ^XTMP("PRCA399",$J,1,0)=0
 S ^XTMP("PRCA399",$J,2,0)=0
 Q
