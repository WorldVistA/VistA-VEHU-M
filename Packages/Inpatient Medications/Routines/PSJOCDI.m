 I PSJSORT=10 D
 . S (PSJDRGIF,PSJONFLG)=1
 . I $P(PSJCLINF,";",2) D DISPCLN^PSJCLNOC(.PSJP,PSJCLINF) Q
 . D DSPDRG(PSJP(4),$P(PSJDNV,U,2),PSJCLINF)
