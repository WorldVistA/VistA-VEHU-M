 . I $P(PSJMONV,U,7)'=PSJSEV S $P(PSJMON(PSJVAGEN,PSJMONTI),U,9)=1
 . S PSJONFG=0
 . S PSJON1=$P($P(PSJMONV,U,6),";")
 . S PSJON2=$P(PSJPON,";")
 . I PSJON1="I",PSJON2'="I" S PSJONFG=1
