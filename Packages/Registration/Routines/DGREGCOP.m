 S FDA(2,IENS,.11572)=DGAR(9)
 S FDA(2,IENS,.11573)=DGAR(10)
 S FDA(2,IENS,.1159)=DGAR(18)
 ; DG*5.3*1127 - Copy MAILING ADDR OVERRIDE IND field to RESIDENTIAL ADDR OVERRIDE IND field .11591
