 ; end truncation
 I $D(RMPRDIR7) D WRITECTL^RMPVIO("!!") S DIR(0)="Y",DIR("A")="Would you like to Mark a Disability Code as Deleted",DIR("B")="NO",DIR("?")="Enter 'Y' to Mark a Disability Code as Deleted" D %DIR^RMPVFM G:$D(DIRUT)!($D(DUOUT))!(+Y=0) END
