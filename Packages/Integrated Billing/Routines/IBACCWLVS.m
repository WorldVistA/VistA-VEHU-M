 S LINEVAR=$$SETFLD^VALM1("Agent Orange Exposure Ind",LINEVAR,"LCAP"),LINEVAR=$$SETFLD^VALM1(DATA,LINEVAR,"LDATA")
 ;I IBRESLT(4) - "SW ASIA CONDITIONS"  ;$$EC^SDCO22(dfn,"")
