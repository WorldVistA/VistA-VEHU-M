 . N DR
 . S DR=$G(VSARRAY(S1,S2,2,"I")) ; disability rating (%)
 . S SORTED(+DR,S2)=""
 ;
 N CNT,BLANK,SF
 S SF=2.04
 S CNT=0,BLANK="                                                    "
 S S1="A" F  S S1=$O(SORTED(S1),-1) Q:S1=""  D    ;get hightest % first
 .S S2="" F  S S2=$O(SORTED(S1,S2)) Q:S2=""  D
 .. S CNT=CNT+1
 .. N D1,D2,D3,D4,D5,D6,D7
 .. N DATA1,DATA2
